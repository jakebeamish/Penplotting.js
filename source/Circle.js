/** Class representing a circle. */
export class Circle {
    /**
     * Create a circle.
     * @param {number} x - The x value of the centre.
     * @param {number} y - The y value of the centre.
     * @param {number} radius - The radius.
     */
    constructor(x, y, radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }
}